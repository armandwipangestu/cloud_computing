/*
Operator:

Pada javascript terdapat beberapa operator diantaranya adalah:

- Assignment Operator = Operator ini digunakan untuk memberikan nilai pada variable. Contohnya, `x = y`
- Comparison Operator = Operator untuk mengevaluasi dan membandingkan dua nilai. Berikut adalah daftar
comparison operator: ==, !=, ===, !==, >, <, >=, <=
- Logical Operator = Operator untuk menetapkan logika yang lebih kompleks. Mirip dengan gerbang logika
pada elektronika. Berikut daftar logical operator: &&, ||, !
*/

